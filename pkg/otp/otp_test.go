package otp

import "testing"

func TestTruncate(t *testing.T) {
	input := []byte{0x1f, 0x86, 0x98, 0x69, 0x0e, 0x02, 0xca, 0x16, 0x61, 0x85, 0x50, 0xef, 0x7f, 0x19, 0xda, 0x8e, 0x94, 0x5b, 0x55, 0x5a}
	expect := 872921

	res := truncate(input)
	if res != expect {
		t.Errorf("truncate method return %d, but want %d", res, expect)
	}
}
